<p>د مفاهیمو د عملي انتقال لپاره باید هر اصل په درې پوړونو تشریح شي: تعریف، ورځنی مثال، او د ارزونې معیار. په دې طریقه لوستونکی په روښانه ډول ګوري چې کوم عادت یې سم دی او کوم عادت اصلاح غواړي. همدا ساده میتود د متن او ژوند ترمنځ واټن راکموي او د روزنې اغېز څو چنده لوړوي.</p>
<h2>د غلطو برداشتونو نقد</h2>
