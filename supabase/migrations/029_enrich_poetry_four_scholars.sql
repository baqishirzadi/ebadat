<p>د دې لید د تثبیت لپاره ګډ کار مهم دی: عالم معیار ورکوي، استاد د انتقال ژبه برابروي، کورنۍ عادتونه پیاوړي کوي او ټولنه د تمرین میدان جوړوي. چې دا زنځیر بشپړ شي، روزنه یوازې فردي حالت نه پاتې کېږي؛ بلکې په ټولنیز کلتور بدلېږي.</p>
